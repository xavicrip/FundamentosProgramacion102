# Funcion que devuelve valor

def sumar(a,b):
    resultado = a+b
    return resultado

print(sumar(2,3))
print(sumar(300,900))
print(sumar(-2,9))
print(sumar(30,-900))

# Funiones que no devuelven valor

def saludar(nombre):
    print("Hola", nombre)

saludar("Wayner")
saludar("Byron")
saludar("Estefany")
saludar("Xavier")
